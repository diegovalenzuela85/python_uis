'''
                            COLECCIONES DE DATOS -- LISTAS - TUPLAS - CONJUNTOS - DICCIONARIOS

    Listas: Se representan con []. Son contenedores de datos totalmente editables.
    Tuplas: Se representan con (). Se caracteiza por que no se pueden modificar desde su declaración. Son mas rapidas en
            ejecucución y consumen menos memoria de nuestra maquina.
    Diccionarios: Se representan con {}. Se caracteriza por que los datos estan dados por llave y valor (key, value)
    Conjuntos: Se representan con {}, pero antes debemos declarar la instancia conjunto set() para que Python interprete
               que es un conjunto. Su caracteriztica principal es que no pueden contener elementos iguales.

    Funciones y Metodos:
    dir() -> Retorna en pantalla las funciones y metodos de los tipos de datos.
    print() -> Retorna en pantalla los resultados del codigo o las iteraciones que realizamos.
    type() -> Presenta en pantalla el tipo de dato que representa la variable. STR-INT-FLOAT-LIST-TUPLE-DICT-SET.
    format() -> Establece formato de salida en la salida de pantalla.
    input() -> Entrada de datos desde consola. Quedan los datos de tipo cadena por defecto.
    int() -> Cambia el valor de tipo cadena a entero. Debe se semejante a un numero.
    float() -> Cambia el valor de tipo cadena a decimal (real). Debe se semejante a un numero.
    str() -> Cambia los valores ingresados a tipo cadena. Deben ser valores numericos.
    bin() -> Convertimos valores enteros a binarios.
    hex() -> Convertimos valores enteros a hexadecimal.
    abs() -> Retorna el valor absoluto de un numero dado.
    round() -> Redondea un numero hacia el valor mas cercano.
    len() -> Retorna en numero de items o caracteres que contiene una cadena, lista, tupla, conjunto, dicconario.
    append() -> Agrega un elemento en la ultima posición, al final del contenedor.
    insert() -> Agrega un elemento en la posición que nosotros le indiquemos dentro del contenedor.
    extend() -> Agrega una serie de valores dentro del contenedor, deben ingresarse desde otra lista.
    in -> Nos permite encontrar un valor en la lista de datos. Se escribe dentro de la función print(). Retorna F o V.
    index() -> Nos indica en que posición esta el valor. Retorna el indice del elemento buscado.
    count() -> Nos retorna en numero de veces que se repite algun dato dentro de la lista.
    pop() -> Permite extraer el ultimo elemento de la lista y luego lo elimina. Si ingresamos el indice lo extrae y luego
             elimina el elemento indicado.
    remove() -> Permite eliminar un elemento dentro de la lista, debemos indicar que elemento deseamos eliminar.
    clear() -> Permite eliminar todos los elementos de una lista. Limpiar lista
    reverse() -> Permite voltear los elementos de una lista. Altera en orden
    sort() -> Permite ordenar los elementos dentro de una lista Ascendentemente por defecto.
    sort(reverse=True) -> Permite ordenar los elementos dentro de una lista Descendentemente.
    list() -> Convierte una colección de datos a lista.
    tuple() -> Convierte una colección de datos a tupla.
    copy() -> Copia una lista

'''

